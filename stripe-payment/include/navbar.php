<script>
    function search() {
  // Get the search query from the input field
      var search_query = document.getElementById("search_query").value;

  // Create an AJAX request to the PHP search script
      var xhttp = new XMLHttpRequest();
      xhttp.onreadystatechange = function() {
        if (this.readyState == 4 && this.status == 200) {
      // Update the search results with the response from the PHP script
          document.querySelector(".search-suggestion").innerHTML = this.responseText;
      }
  };
  xhttp.open("POST", "search.php", true);
  xhttp.setRequestHeader("Content-type", "application/x-www-form-urlencoded");
  xhttp.send("search_query=" + search_query);
}

</script>
